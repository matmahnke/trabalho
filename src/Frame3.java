    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pasta pasta = new Pasta();
        pasta.setNome(txtNomePasta.getText());
        pasta.setDiretorio(comp.getNome() + "/" + txtNomePasta.getText());
        pasta.setTamanho(txtNomePasta.getText().length() * 8);
        txtPastaAtual.setText(txtNomePasta.getText());
        if (isUSB) {
            USB[] usb = comp.getUS();
            List<Pasta> pastas = comp.getUS()[id].getPastas();
            pastas.add(pasta);
            usb[id].setPastas(pastas);
        } else {
            Paralela[] par = comp.getPAR();
            List<Pasta> pastas = comp.getUS()[id].getPastas();
            pastas.add(pasta);
            par[id].setPastas(pastas);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void btCriarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCriarArquivoActionPerformed
        List<AlocacaoDeArquivos> arqs;
        Arquivo arq = new Arquivo();
        arq.setDiretorio(comp.getNome() + "/" + txtNomeArquivo.getText());
        arq.setExtensao(txtExtensao.getText());
        arq.setNome(txtNomeArquivo.getText());
        arq.setTamanho(txtNomeArquivo.getName().length() * 8);
        if (isUSB) {
            USB[] usb = comp.getUS();
            List<Pasta> p = usb[id].getPastas();
            arqs = p.get(p.size()).getArquivos();
            arqs.add(arq);
            p.get(p.size()).setArquivos(arqs);
        } else {
            Paralela[] par = comp.getPAR();
            List<Pasta> p = par[id].getPastas();
            arqs = p.get(p.size()).getArquivos();
            arqs.add(arq);
            p.get(p.size()).setArquivos(arqs);
        }
    }//GEN-LAST:event_btCriarArquivoActionPerformed

